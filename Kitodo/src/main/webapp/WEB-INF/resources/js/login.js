function setLanguageCookieAndReload( lang ) {
    const d = new Date();
    d.setTime(d.getTime() + (365 * 24 * 60 * 60 * 1000));
    let expires = "expires="+d.toUTCString();
    document.cookie = "kitodo_lang=" + lang + ";" + expires + ";path=/kitodo";
    location.reload();
}
